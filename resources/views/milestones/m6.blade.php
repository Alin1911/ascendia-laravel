<h4>3. Prezentarea competiției</h4>
<ol>
    <li>Platforme de mentorat online locale (directe):</li>
</ol>
<p>Aceste platforme oferă servicii de mentorat sau coaching &icirc;n diverse forme. Deși cotele de piață exacte nu sunt
    disponibile, estimările se bazează pe notorietatea și accesibilitatea platformelor.</p>
<p>Coachinghub.com</p>
<p><strong>Descriere</strong>: Axată pe sesiuni de coaching personalizat pentru dezvoltare profesională și personală.
</p>
<p><strong>Piață țintă</strong>: Profesioniști care caută &icirc;mbunătățirea performanțelor și echilibrului personal.
</p>
<p><strong>Market share estimat</strong>: ~10% din piața de coaching/mentorat, bazat pe prezența și activitatea online.
</p>
<p>MentorMind</p>
<p><strong>Descriere</strong>: Conectează mentori experimentați cu tineri pentru dezvoltare profesională.</p>
<p><strong>Piață țintă</strong>: Tineri și studenți &icirc;n căutarea unor mentori din domenii specifice.</p>
<p><strong>Market share estimat:</strong> ~5%, av&acirc;nd o nișă restr&acirc;nsă și specifică.</p>
<p>Life Coaching Rom&acirc;nia</p>
<p><strong>Descriere</strong>: Oferă sesiuni de coaching individuale, cu accent pe dezvoltare personală.</p>
<p><strong>Piață țintă</strong>: Persoane interesate de coaching &icirc;n viața personală și profesională.</p>
<p><strong>Market share estimat</strong>: ~3%, av&acirc;nd o prezență limitată online.</p>
<p>&nbsp;</p>
<ol start="2">
    <li>Platforme internaționale aplicabile &icirc;n Rom&acirc;nia:</li>
</ol>
<p>Aceste platforme sunt utilizate de rom&acirc;ni, dar au un focus global. Market share-ul lor &icirc;n Rom&acirc;nia
    este dificil de cuantificat, fiind parțial preluate din piața internațională.</p>
<p>LinkedIn</p>
<p><strong>Descriere</strong>: Conectează utilizatorii LinkedIn pentru sfaturi de carieră și mentorat.</p>
<p><strong>Piață țintă</strong>: Profesioniști care utilizează LinkedIn activ.</p>
<p><strong>Market share estimat (Rom&acirc;nia):</strong> ~15%, datorită bazei mari de utilizatori LinkedIn &icirc;n
    Rom&acirc;nia (~3 milioane de utilizatori).</p>
<p>Clarity.fm</p>
<p><strong>Descriere:</strong> Platformă globală care oferă mentorat plătit, per minut.</p>
<p><strong>Piață țintă</strong>: Profesioniști dispuși să plătească pentru expertiză &icirc;n timp real.</p>
<p><strong>Market share estimat (Rom&acirc;nia):</strong> ~5%, utilizată &icirc;n special de freelanceri și
    antreprenori.</p>
<p>&nbsp;</p>
<ol start="3">
    <li>Platforme de freelancing relevante:</li>
</ol>
<p>Platformele de freelancing oferă oportunități indirecte pentru mentorat, fiind utilizate și de freelanceri
    rom&acirc;ni.</p>
<p>Upwork, Fiverr, Freelancer</p>
<p><strong>Descriere</strong>: Freelanceri care oferă sesiuni de mentorat sau coaching.</p>
<p><strong>Piață țintă</strong>: Profesioniști internaționali și locali &icirc;n căutare de mentori sau freelanceri.</p>
<p><strong>Market share estimat (Rom&acirc;nia):</strong> ~20%, av&acirc;nd o bază largă de utilizatori și popularitate.
</p>
<p>&nbsp;</p>
<p>Estimări generale:</p>
<p>Dintr-o piață estimată de ~25.500 - 127.500 utilizatori activi (mentees):</p>
<p>Platformele locale: Acoperă aproximativ 30% din piață, datorită focusului pe Rom&acirc;nia.</p>
<p>Platformele internaționale: Acoperă aproximativ 40% din piață, datorită notorietății globale.</p>
<p>Platformele de freelancing: Acoperă restul de 30%, fiind utilizate indirect pentru mentorat.</p>
<p>Aceste date arată că există un spațiu semnificativ pentru o aplicație precum Ascendia să &icirc;și revendice o cotă
    de piață competitivă, mai ales datorită modelului unic de freelancing pentru mentori.</p>
