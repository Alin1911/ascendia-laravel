<h4>1.&nbsp; Dimensiunea pieții țintă</h4>
<p>Aplicația Ascendia, care facilitează identificarea mentorilor și organizarea sesiunilor de mentorat, se adresează
    unei piețe &icirc;n creștere &icirc;n Rom&acirc;nia.</p>
<ul>
    <li>Piața mentoratului online &icirc;n Rom&acirc;nia:</li>
</ul>
<p>Deși nu există date statistice precise privind dimensiunea exactă a pieței de mentorat online &icirc;n Rom&acirc;nia,
    interesul pentru astfel de servicii este &icirc;n creștere. De exemplu, platforma ProMe, lansată de SIVECO,
    facilitează comunicarea și colaborarea &icirc;ntre diferite generații profesionale, oferind persoanelor mai &icirc;n
    v&acirc;rstă posibilitatea de a &icirc;mpărtăși tinerilor din experiența lor profesională. (<a
        href="https://www.ceccarbusinessmagazine.ro/a-fost-lansata-platforma-de-mentorat-prome-professional-intergenerational-cooperation-and-mentoring/a/NTQ2MTMyMTA0MzQ5NzE2M5KZhV6ASiwAlFiYa8lmrjs">https://www.ceccarbusinessmagazine.ro/a-fost-lansata-platforma-de-mentorat-prome-professional-intergenerational-cooperation-and-mentoring/a/NTQ2MTMyMTA0MzQ5NzE2M5KZhV6ASiwAlFiYa8lmrjs</a>)
</p>
<ul>
    <li>Piața freelancerilor &icirc;n Rom&acirc;nia:</li>
</ul>
<p>Conform datelor Eurostat, &icirc;n 2020, Rom&acirc;nia avea peste 1,2 milioane de liber-profesioniști cu v&acirc;rste
    cuprinse &icirc;ntre 15 și 64 de ani, reprezent&acirc;nd aproximativ 14% din forța de muncă. (<a
        href="https://www.wall-street.ro/articol/Careers/273023/peste-1-2-milioane-de-freelenaceri-au-lucrat-in-romania-in-2020-care-sunt-avantajele-si-dezavantajele-acestui-mod-de-lucru.html">https://www.wall-street.ro/articol/Careers/273023/peste-1-2-milioane-de-freelenaceri-au-lucrat-in-romania-in-2020-care-sunt-avantajele-si-dezavantajele-acestui-mod-de-lucru.html</a>)&nbsp;
</p>
<p>&nbsp;Această tendință a continuat, iar &icirc;n 2024, procentajul de freelanceri din țară a atins cota de 17,2%. (<a
        href="https://business.olx.ro/blog/ghid-freelancing-romania/">https://business.olx.ro/blog/ghid-freelancing-romania/</a>)&nbsp;
    Acești freelanceri activează &icirc;n diverse domenii, inclusiv IT, marketing, servicii creative și educație, și
    reprezintă un segment semnificativ al pieței muncii.</p>
<ul>
    <li>Piața potențială pentru aplicația Ascendia:</li>
</ul>
<p>Av&acirc;nd &icirc;n vedere că aplicația Ascendia permite oricui să devină mentor după o evaluare și că oricine poate
    achita pentru sesiuni de mentorat, piața țintă include:</p>
<ul>
    <li>Mentori potențiali &ndash; profesioniști cu experiență &icirc;n diverse domenii, interesați să ofere mentorat
        &icirc;n regim freelance.</li>
    <li>Mentees (beneficiari) &ndash; persoane care doresc să &icirc;și dezvolte competențele și să &icirc;nvețe de la
        experți, dispuse să plătească pentru sesiuni de mentorat.</li>
</ul>
<p>Deși nu există date statistice precise privind dimensiunea exactă a pieței de mentorat online &icirc;n Rom&acirc;nia,
    creșterea numărului de freelanceri și interesul sporit pentru dezvoltarea profesională sugerează un potențial
    semnificativ.</p>
<p>Pentru a estima dimensiunea pieței țintă a aplicației Ascendia, vom combina date disponibile despre numărul de
    freelanceri, profesioniști și utilizatori interesați de mentorat &icirc;n Rom&acirc;nia. Aprobările noastre vor
    include cifre concrete și raționamente bazate pe tendințele pieței.</p>
<p>&nbsp;</p>
<p>Estimarea pieței totale:</p>
<ol>
    <li>Mentori potențiali</li>
</ol>
<p>&Icirc;n Rom&acirc;nia, sunt 1,2 milioane de freelanceri (conform Eurostat, 2020). Estimăm că aproximativ 10% dintre
    aceștia sunt dispuși să ofere mentorat datorită experienței și dorinței de a-și diversifica sursele de venit.
    Aceasta &icirc;nseamnă aproximativ 120.000 de mentori potențiali.</p>
<ol start="2">
    <li>Mentees potențiali:</li>
</ol>
<p>Numărul de persoane interesate de dezvoltare personală și profesională este &icirc;n creștere. Conform unui sondaj
    realizat de Statista &icirc;n 2022, 30% din populația activă (forța de muncă) din Rom&acirc;nia investește &icirc;n
    cursuri de formare profesională. Forța de muncă din Rom&acirc;nia numără aproximativ 8,5 milioane de persoane (INS,
    2023). Astfel, avem 2,55 milioane de potențiali mentees interesați de sesiuni de mentorat.</p>
<ol start="3">
    <li>Piața de freelanceri globală (de referință pentru Rom&acirc;nia):</li>
</ol>
<p>Global, mentoratul online este &icirc;n plină expansiune. Dimensiunea pieței globale de mentorat online era evaluată
    la 356 milioane USD &icirc;n 2022, cu o creștere anuală de 17,5% p&acirc;nă &icirc;n 2030 (Allied Market Research).
    Rom&acirc;nia reprezintă aproximativ 1% din această piață globală, ceea ce implică un potențial de 3,56 milioane USD
    anual, aproximativ 18 milioane de RON.</p>
<p>&nbsp;</p>
<p><strong>Dimensiunea pieței (aproximare)</strong></p>
<p>Din cele 2,55 milioane de mentees potențiali, estimăm că aproximativ 1% - 5% ar utiliza activ o aplicație precum
    Ascendia, ceea ce &icirc;nseamnă: 25.500 &ndash; 127.500 de mentees activi.</p>
<p>Din cei 120.000 de mentori potențiali, un procent similar (1% - 5%) ar deveni mentori activi, ceea ce &icirc;nseamnă:
    1.200 &ndash; 6.000 de mentori activi.</p>
