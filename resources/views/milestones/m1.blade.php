       <h2 class="accordion-header" id="m1-problem"><button
             class="accordion-button collapsed rounded-0" type="button" data-bs-toggle="collapse"
             data-bs-target="#m1-problem-content" aria-expanded="false"
             aria-controls="m1-problem-content"> 3.PROBLEMA IDENTIFICATĂ </button></h2>
       <div id="m1-problem-content" class="accordion-collapse collapse"
          aria-labelledby="m1-problem">
          <div class="accordion-body">
             <p> Pe măsură ce oamenii avansează în carieră sau își propun să crească într-un anumit
                domeniu, ajung să realizeze că au nevoie de un mentor – o persoană cu mai multă
                experiență care să le ofere ghidare și sfaturi valoroase. Cu această dorință bine
                definită, ei se confruntă însă cu o nouă provocare: <strong>cum să găsească un
                   mentor potrivit?</strong></p>
             <p> Deși există mentori capabili, mulți nu știu unde să îi caute. Platformele actuale
                nu oferă metode clare de verificare a experienței și relevanței mentorilor, iar
                această lipsă de transparență le îngreunează căutarea. Astfel, dorința de a învăța
                și de a se dezvolta se transformă într-un proces frustrant, lăsându-i pe oameni
                blocați între <strong>nevoia de îndrumare</strong> și <strong>dificultatea de a găsi
                   un mentor potrivit</strong> care să îi ajute cu adevărat. </p>
             <p> În afară de lipsa de transparență a platformelor existente și dificultățile în a
                găsi mentori capabili, mai există și alte provocări: </p>
             <ul>
                <li>Contactarea unui potențial mentor poate fi o experiență intimidantă. Mulți nu
                   știu cum să se prezinte sau să-și exprime nevoile astfel încât să obțină
                   interesul mentorului.</li>
                <li>În anumite domenii de specializare, este adesea greu de găsit mentori
                   disponibili și calificați. Aceasta poate reduce opțiunile pentru cei care caută
                   îndrumare în nișe foarte specifice, făcând procesul de căutare și mai dificil.
                </li>
                <li>Chiar dacă un mentor are expertiză, este esențial ca stilurile lor de lucru și
                   obiectivele să fie aliniate cu cele ale mentee-ului. Lipsa acestei
                   compatibilități poate duce la o relație de mentorat ineficientă.</li>
                <li>Oamenii nu dispun întotdeauna de instrumentele necesare pentru a evalua
                   calificările și reputația mentorilor, ceea ce poate duce la alegeri greșite.</li>
                <li>Mentee-ii caută relații personalizate cu mentorii. Aceștia doresc să beneficieze
                   de o îndrumare adaptată contextului lor individual.</li>
             </ul>
             <p> Experiența de a găsi un mentor poate fi una provocatoare, iar emoțiile oamenilor
                joacă un rol esențial în această căutare. <strong>Frustrarea</strong> și
                <strong>anxietatea</strong> apar frecvent în acest proces, iar nesiguranța în
                abordarea mentorilor sau <strong>teama de un refuz</strong> pot diminua încrederea
                în propriile abilități. Aceste sentimente afectează nu doar motivația, ci și
                capacitatea de a-și atinge obiectivele profesionale. În plus, eforturile nereușite
                de a obține îndrumare generează <strong>dezamăgire</strong>, amplificând impresia că
                dezvoltarea personală devine o provocare insurmontabilă.
             </p>
             <p> Conform articolului publicat de Forbes <a
                   href="https://www.forbes.com/sites/christinecomaford/2019/07/03/new-study-76-of-people-think-mentors-are-important-but-only-37-have-one/#:~:text=They%20recently%20surveyed%203%2C000%20people%20about%20professional%20mentor-mentee,however%2C%20only%2037%25%20of%20people%20currently%20have%20one.">[1]</a>,
                cercetarea efectuată de Olivet Nazarene University arată că 76% dintre respondenți
                consideră că este esențial să aibă un mentor, dar doar 37% dintre aceștia
                beneficiază efectiv de îndrumarea unui mentor. </p>
             <p>Această discrepanță semnificativă între percepția valorii mentoratului și realitatea
                accesului sugerează o problemă majoră în disponibilitatea și accesibilitatea
                mentorilor calificați. Oamenii recunosc importanța îndrumării, dar se confruntă cu
                obstacole în a găsi și a stabili conexiuni cu mentorii.</p>
