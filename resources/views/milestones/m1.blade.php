       <div id="m1-solution-content" class="accordion-collapse collapse"
          aria-labelledby="m1-solution">
          <div class="accordion-body">
             <p> Pentru a aborda aceste dificultăți și a răspunde nevoii tot mai mari de îndrumare
                profesională și personală, propunem o platformă online de mentorat, în care oamenii
                pot găsi rapid mentori calificați și disponibili, care să le ofere îndrumare
                personalizată. Pe această platformă, mentorii își vor putea seta propriile tarife
                pentru serviciile oferite, iar mentee-ii vor avea posibilitatea de a alege mentorul
                potrivit pe baza competențelor, experienței și recenziilor primite de la alți
                utilizatori. </p>
             <strong>Platforma va include:</strong>
             <ul>
                <li>Profiluri detaliate ale mentorilor – care să permită utilizatorilor să vadă
                   calificările, experiența profesională, domeniul de expertiză și feedback-ul de la
                   alți mentee-i. Astfel, se adresează problema lipsei de transparență a
                   platformelor actuale.</li>
                <li>Filtre de căutare și compatibilitate – care să permită mentee-ilor să găsească
                   mentorul potrivit în funcție de nevoile lor specifice, de nișe și de obiective
                   profesionale.</li>
                <li>Sesiuni de mentorat plătite – mentorii își vor stabili tarifele, permițând o
                   gamă diversificată de prețuri pentru toate bugetele. Astfel, se oferă
                   flexibilitate atât pentru mentori, cât și pentru mentee-i.</li>
                <li>Evaluare și verificare – recenzii și recomandări din partea utilizatorilor
                   anteriori, asigurând calitatea și relevanța serviciilor de mentorat.</li>
             </ul>
             <p> Această soluție creează un spațiu transparent, accesibil și eficient pentru
                interacțiuni mentor-mentee, făcând procesul de căutare a mentorilor mai puțin
                intimidant și mai structurat. Platforma propusă reduce frustrarea asociată cu
                găsirea unui mentor și asigură accesul facil la mentorat valoros și de încredere.
             </p>
