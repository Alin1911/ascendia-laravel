<h3><strong>Scopul MVP</strong></h3>
<p><span style="font-weight: 400;">Scopul acestui MVP este de a crea o platformă funcțională care să permită conectarea mentee-urilor cu mentorii, astfel &icirc;nc&acirc;t să valideze ideea de bază: utilizatorii sunt interesați să folosească platforma pentru mentorat și sunt dispuși să plătească pentru sesiuni. MVP-ul trebuie să ofere valoare utilizatorilor &icirc;ntr-un mod simplu, dar complet.</span></p>
<h3><strong>Caracteristici incluse &icirc;n MVP</strong></h3>
<h4><strong>Pentru Mentee:</strong></h4>
<ol>
<li style="font-weight: 400;"><strong>Crearea și autentificarea contului:</strong></li>
<ul>
<li style="font-weight: 400;"><span style="font-weight: 400;">Funcționalitate de creare a unui cont nou.</span></li>
<li style="font-weight: 400;"><span style="font-weight: 400;">Autentificare cu email și parolă.</span></li>
</ul>
<li style="font-weight: 400;"><strong>Căutarea mentorilor:</strong></li>
<ul>
<li style="font-weight: 400;"><span style="font-weight: 400;">Posibilitatea de a căuta mentori folosind cuvinte-cheie.</span></li>
<li style="font-weight: 400;"><span style="font-weight: 400;">Filtre: expertiză, interval de preț (per oră), disponibilitate imediată ("Available Now").</span></li>
</ul>
<li style="font-weight: 400;"><strong>Vizualizarea profilurilor mentorilor:</strong></li>
<ul>
<li style="font-weight: 400;"><span style="font-weight: 400;">Informații afișate: experiență, domeniu de expertiză, descriere personală, realizări.</span></li>
</ul>
<li style="font-weight: 400;"><strong>Rezervarea unei sesiuni:</strong></li>
<ul>
<li style="font-weight: 400;"><span style="font-weight: 400;">Mentee-ul poate rezerva o sesiune din profilul mentorului.</span></li>
<li style="font-weight: 400;"><span style="font-weight: 400;">Confirmarea rezervării printr-un mesaj vizibil.</span></li>
</ul>
<li style="font-weight: 400;"><strong>Gestionarea sesiunilor active și finalizate:</strong></li>
<ul>
<li style="font-weight: 400;"><span style="font-weight: 400;">Vizualizarea sesiunilor achiziționate &icirc;n categoriile "Active" și "Closed".</span></li>
<li style="font-weight: 400;"><span style="font-weight: 400;">Opțiunea de a intra &icirc;n sesiuni active ("Join Session") și accesul la chat.</span></li>
</ul>
<li style="font-weight: 400;"><strong>Mesagerie:</strong></li>
<ul>
<li style="font-weight: 400;"><span style="font-weight: 400;">Funcționalitate de chat cu mentorul pentru sesiunile active.</span></li>
<li style="font-weight: 400;"><span style="font-weight: 400;">Istoric al mesajelor.</span></li>
</ul>
</ol>
<h4><strong>Pentru Mentor:</strong></h4>
<ol>
<li style="font-weight: 400;"><strong>Autentificarea contului:</strong></li>
<ul>
<li style="font-weight: 400;"><span style="font-weight: 400;">Funcționalitate de autentificare (similara cu cea a mentee-ului).</span></li>
</ul>
<li style="font-weight: 400;"><strong>Vizualizarea sesiunilor rezervate de mentee-uri:</strong></li>
<ul>
<li style="font-weight: 400;"><span style="font-weight: 400;">Listarea sesiunilor active și finalizate.</span></li>
</ul>
<li style="font-weight: 400;"><strong>Participarea la sesiuni:</strong></li>
<ul>
<li style="font-weight: 400;"><span style="font-weight: 400;">Accesarea chat-ului pentru a comunica cu mentee-ul &icirc;n cadrul sesiunilor active.</span></li>
</ul>
<li style="font-weight: 400;"><strong>Gestionarea informațiilor despre mentee-uri:</strong></li>
<ul>
<li style="font-weight: 400;"><span style="font-weight: 400;">Posibilitatea de a vedea istoricul interacțiunilor cu fiecare mentee.</span></li>
</ul>
</ol>
<h3><strong>Caracteristici excluse din MVP</strong></h3>
<ol>
<li style="font-weight: 400;"><span style="font-weight: 400;">Plata efectivă pentru sesiuni (implementată doar ca un flux simulativ).</span></li>
<li style="font-weight: 400;"><span style="font-weight: 400;">Posibilitatea ca mentorii să și creeze singuri profilurile.</span></li>
<li style="font-weight: 400;"><span style="font-weight: 400;">Sistem de notițe pentru mentee-uri.</span></li>
<li style="font-weight: 400;"><span style="font-weight: 400;">Limitarea timpului pentru sesiuni sau mecanisme avansate de plată.</span></li>
<li style="font-weight: 400;"><span style="font-weight: 400;">Sistem complet de evaluări și recenzii pentru mentori.</span></li>
</ol>
<h3><strong>Justificări pentru Caracteristicile MVP</strong></h3>
<h4><strong>1. Crearea și autentificarea contului</strong></h4>
<p><span style="font-weight: 400;">Aceasta este o funcționalitate esențială pentru utilizatori pentru a accesa platforma și a naviga &icirc;n siguranță. Permite colectarea de feedback valoros despre utilizarea platformei.</span></p>
<h4><strong>2. Căutarea mentorilor și utilizarea filtrelor</strong></h4>
<p><span style="font-weight: 400;">Aceasta reprezintă punctul central al platformei, permiț&acirc;nd mentee-urilor să găsească mentori potriviți pentru nevoile lor. Este un pas critic pentru validarea interesului utilizatorilor.</span></p>
<h4><strong>3. Rezervarea sesiunilor</strong></h4>
<p><span style="font-weight: 400;">Aceasta este o funcționalitate cheie, care demonstrează cum platforma poate conecta mentee-urile și mentorii, oferind valoare imediată.</span></p>
<h4><strong>4. Chat-ul pentru sesiuni active</strong></h4>
<p><span style="font-weight: 400;">Chat-ul asigură comunicarea &icirc;n timp real și creează o bază pentru colaborare. De asemenea, istorică contribuie la continuitatea interacțiunilor.</span></p>
<h4><strong>5. Vizualizarea sesiunilor active și finalizate</strong></h4>
<p><span style="font-weight: 400;">Aceasta ajută utilizatorii să știe ce sesiuni au &icirc;nceput sau s-au &icirc;nceiat, fiind o funcționalitate de bază pentru organizare.</span></p>
<h3><strong>Cum MVP-ul urmează principiile unui produs minim viabil</strong></h3>
<ol>
<li style="font-weight: 400;"><strong>Valoare inițială pentru utilizatori:</strong></li>
<ul>
<li style="font-weight: 400;"><span style="font-weight: 400;">Utilizatorii pot deja să caute mentori, să vizualizeze informații relevante și să rezerve sesiuni, av&acirc;nd acces la o funcționalitate semnificativă.</span></li>
</ul>
<li style="font-weight: 400;"><strong>Demonstrarea beneficiilor viitoare:</strong></li>
<ul>
<li style="font-weight: 400;"><span style="font-weight: 400;">Chiar dacă plata și alte funcționalități mai complexe nu sunt implementate, MVP-ul prezintă clar utilitatea platformei, atrăg&acirc;nd potențiali utilizatori.</span></li>
</ul>
<li style="font-weight: 400;"><strong>Feedback:</strong></li>
<ul>
<li style="font-weight: 400;"><span style="font-weight: 400;">Utilizatorii pot oferi feedback despre fluxurile implementate (căutare, chat, rezervare), care vor ghida dezvoltarea ulterioară.</span></li>
</ul>
</ol>
<h3><strong>Pașii Următori</strong></h3>
<ol>
<li style="font-weight: 400;"><strong>Colectarea feedback-ului:</strong></li>
<ul>
<li style="font-weight: 400;"><span style="font-weight: 400;">Evaluarea modului &icirc;n care utilizatorii percep experiența platformei.</span></li>
<li style="font-weight: 400;"><span style="font-weight: 400;">Identificarea punctelor slabe și a funcționalităților prioritare pentru următorul ciclu de dezvoltare.</span></li>
</ul>
<li style="font-weight: 400;"><strong>Dezvoltarea funcționalităților adiționale:</strong></li>
<ul>
<li style="font-weight: 400;"><span style="font-weight: 400;">Implementarea plății efective.</span></li>
<li style="font-weight: 400;"><span style="font-weight: 400;">Adăugarea unui sistem complet de recenzii.</span></li>
<li style="font-weight: 400;"><span style="font-weight: 400;">Automatizarea procesului de creare a profilurilor pentru mentori.</span></li>
</ul>
<li style="font-weight: 400;"><strong>Testarea extensivă:</strong></li>
<ul>
<li style="font-weight: 400;"><span style="font-weight: 400;">Validarea &icirc;n profunzime a funcționalităților MVP pentru a elimina eventualele erori.</span></li>
</ul>
</ol>
<p><span style="font-weight: 400;">Prin construirea acestui MVP, platforma poate valida conceptul de bază, adun&acirc;nd informații esențiale pentru a ghida dezvoltarea ulterioară. Aceasta permite iterarea rapidă și cost-eficientă &icirc;n direcția unei platforme complete și sustenabile.</span></p>